
public class UseVariable {
	public static void main(String[] args){
		int num1;
		num1 = 10;
		
		int num2 = 30;
		int num3 = num1 + num2;
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		
		// byte = 1 byte
		// short = 2 byte
		// int = 4 byte
		// long = 8 byte
		
		// float = 4 byte
		// double = 8 byte
	}
}
